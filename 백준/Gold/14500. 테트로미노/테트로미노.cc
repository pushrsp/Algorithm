int get_count(int _y, int _x, int k) {
    int ret = 0;
    for (int y = 0; y < 4; ++y) {
        for (int x = 0; x < 4; ++x) {
            ret += (block[k][y][x] - '0') * MAP[y + _y][x + _x];
        }
    }

    return ret;
}

